package com.future.my.board.vo;


public class BoardSearchVO extends PagingVO {
	
    private String searchWord;  // 검색어
    private String searchType;  // 검색어
    private String useYn;       // 사용 여부
    private String memId;       // 작성자 ID
    private String startDate;   // 시작 날짜
    private String endDate;     // 종료 날짜
	
    public String getSearchWord() {
		return searchWord;
	}
	public void setSearchWord(String searchWord) {
		this.searchWord = searchWord;
	}
	public String getUseYn() {
		return useYn;
	}
	public void setUseYn(String useYn) {
		this.useYn = useYn;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getSearchType() {
		return searchType;
	}
	public void setSearchType(String searchType) {
		this.searchType = searchType;
	}
	@Override
	public String toString() {
		return "BoardSearchVO [searchWord=" + searchWord + ", useYn=" + useYn + ", memId=" + memId + ", startDate="
				+ startDate + ", endDate=" + endDate + "]";
	}
	
}
	
 